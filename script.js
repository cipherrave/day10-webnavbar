function openSideNav() {
  document.getElementById("mobileList").style.display = "block";
  document.getElementById("mobileList").style.transition =
    "width 1s, height 1s";
}

function closeSideNav() {
  document.getElementById("mobileList").style.display = "none";
  document.getElementById("mobileList").style.transition =
    "width 1s, height 1s";
}
